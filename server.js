const { GoogleGenerativeAI } = require('@google/generative-ai');

// –ù–û–í–ï: –ü—ñ–¥–∫–ª—é—á–∞—î–º–æ Firebase Admin SDK –¥–ª—è —Ä–æ–±–æ—Ç–∏ –∑ –±–∞–∑–æ—é –¥–∞–Ω–∏—Ö
