// --- –ù–∞–ª–∞—à—Ç—É–≤–∞–Ω–Ω—è Firebase ---
// –ù–û–í–ï: –ó–∞–≤–∞–Ω—Ç–∞–∂—É—î–º–æ –Ω–∞—à –∫–ª—é—á –¥–æ –±–∞–∑–∏ –¥–∞–Ω–∏—Ö
const serviceAccount = require('./serviceAccountKey.json');

// –ù–û–í–ï: –Ü–Ω—ñ—Ü—ñ–∞–ª—ñ–∑—É—î–º–æ –¥–æ–¥–∞—Ç–æ–∫ Firebase, –Ω–∞–¥–∞–≤—à–∏ –π–æ–º—É "–∫–ª—é—á"
